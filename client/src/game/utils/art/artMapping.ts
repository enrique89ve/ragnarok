/**
 * Look up the asset id for any kind of game entity.
 *
 *   numeric / numeric-string ids   → cards, served from ART_REGISTRY
 *   'hero-X'                       → heroes, served from ALL_NORSE_HEROES[id].portrait
 *   'king-X'                       → kings,  served from NORSE_KINGS[id].portrait
 *
 * The hero/king path is the single source of truth; ART_REGISTRY no longer
 * holds entity portraits — the `portrait` field on each entity is canon.
 */
function lookupAssetId(id: string | number | undefined): string | null {
	if (id == null || id === '') return null;
	const key = typeof id === 'number' ? String(id) : id;
	if (key.startsWith('hero-')) return ALL_NORSE_HEROES[key]?.portrait ?? null;
	if (key.startsWith('king-')) return NORSE_KINGS[key]?.portrait ?? null;
	const raw = ART_REGISTRY[key];
	if (!raw) return null;
	const m = raw.match(/\/art\/nfts\/([^/]+)\.webp$/);
	return m ? m[1] : null;
}

function resolve(id: string | number | undefined): string | null {
	if (id == null || id === '') return null;
	const key = typeof id === 'number' ? String(id) : id;
	if (_cache.has(key)) return _cache.get(key)!;
	const assetId = lookupAssetId(id);
	const result = assetId ? assetPath(`/art/nfts/${assetId}.webp`) : null;
	_cache.set(key, result);
	return result;
}

function rawPath(id: string | number | undefined): string | null {
	const assetId = lookupAssetId(id);
	return assetId ? `/art/nfts/${assetId}.webp` : null;
}

function bareId(path: string | null): string | null {
	if (!path) return null;
	const m = path.match(/\/art\/nfts\/([^/]+)\.webp$/);
	return m ? m[1] : null;
}

// ── Public API ──

export function getCardArtPath(_cardName: string, cardId?: number | string): string | null {
	return resolve(cardId);
}

export function getRawCardArtPath(_cardName: string, cardId?: number | string): string | null {
	return rawPath(cardId);
}

export function getCardArtById(cardId: number | string): string | null {
	return resolve(cardId);
}

export function getHeroArtId(heroId: string): string | null {
	return bareId(rawPath(heroId));
}

export function getKingArtId(kingId: string): string | null {
	return bareId(rawPath(kingId));
}

export function getHeroArtPath(heroId: string): string | null {
	return resolve(heroId);
}

export function getKingArtPath(kingId: string): string | null {
	return resolve(kingId);
}

export function getCharacterArtPath(id: string): string | null {
	if (id.startsWith('hero-') || id.startsWith('king-')) return resolve(id);
	return null;
}

export function resolveHeroPortrait(heroId?: string, explicitPortrait?: string): string | undefined {
	if (heroId) {
		const r = resolve(heroId);
		if (r) return r;
	}
	if (explicitPortrait) return assetPath(explicitPortrait);
	return undefined;
}
