/**
 * Look up the bare `AssetId` for any kind of game entity.
 *
 *   numeric / numeric-string ids   → cards, served from `ART_REGISTRY`
 *   'hero-X'                       → heroes, served from `ALL_NORSE_HEROES[id].portrait`
 *   'king-X'                       → kings,  served from `NORSE_KINGS[id].portrait`
 *
 * Returns `null` when the id is unknown or its stored value is not a valid
 * asset id (caught at the trust boundary by `tryAssetId`).
 */
function lookupAssetId(id: string | number | undefined): AssetId | null {
	if (id == null || id === '') return null;
	const key = typeof id === 'number' ? String(id) : id;
	if (key.startsWith('hero-')) {
		const portrait = ALL_NORSE_HEROES[key]?.portrait;
		return portrait ? tryAssetId(portrait) : null;
	}
	if (key.startsWith('king-')) {
		const portrait = NORSE_KINGS[key]?.portrait;
		return portrait ? tryAssetId(portrait) : null;
	}
	const raw = ART_REGISTRY[key as `${number}`];
	return raw ? tryAssetId(raw) : null;
}

const _cache = new Map<string, string | null>();
if (import.meta.hot) {
	import.meta.hot.accept(() => _cache.clear());
}

function resolve(id: string | number | undefined): string | null {
	if (id == null || id === '') return null;
	const key = typeof id === 'number' ? String(id) : id;
	const cached = _cache.get(key);
	if (cached !== undefined) return cached;
	const assetId = lookupAssetId(id);
	const result = assetId ? assetPath(assetPathFor(assetId)) : null;
	_cache.set(key, result);
	return result;
}

// ── Public API ────────────────────────────────────────────────────────────

export function getCardArtPath(cardId: number | string | undefined): string | null {
	return resolve(cardId);
}

/**
 * Card art path WITHOUT the Vite `BASE_URL` prefix. Used at the on-chain mint
 * boundary where the path needs to be `BASE_URL`-independent (the chain stores
 * a raw site-relative path).
 */
export function getRawCardArtPath(cardId: number | string | undefined): string | null {
	const assetId = lookupAssetId(cardId);
	return assetId ? assetPathFor(assetId) : null;
}

export function getHeroArtPath(heroId: string): string | null {
	return resolve(heroId);
}

export function getKingArtPath(kingId: string): string | null {
	return resolve(kingId);
}

export function resolveHeroPortrait(heroId?: string, explicitPortrait?: string): string | undefined {
	if (heroId) {
		const r = resolve(heroId);
		if (r) return r;
	}
	if (explicitPortrait) return assetPath(explicitPortrait);
	return undefined;
}
